package com.tsl.more_operations_with_arrays;


/**
 * MathUtilities encapsulates some methods that perform common mathematical operations.
 * @author Tom Lever
 * @version 1.0
 * @since 05/18/21
 */
class MathUtilities {

	
	/**
	 * isOdd indicates whether an integer is odd or not.
	 * @param theInteger
	 * @return
	 */
	public static boolean isOdd(int theInteger) {
		return (theInteger % 2 != 0);
	}
	
}
